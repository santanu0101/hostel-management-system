    <script>

        showdata();
        function showdata() {
            //Get all Form Input Data

            $.ajax({
                url: "student_data_show.php",
            }).done(function (data) {
                $('#result').html(data);
                //data == response
            });

        }

        $('#simple-search').on('input', function () {
            // console.log(this.value);
            const inputvalue = $(this).val();
            console.log(inputvalue);
            $.ajax({
                type: "POST",
                url: "student_data_show.php",
                data: { inputvalue: inputvalue },
            }).done(function (data) {
                $('#result').html(data);
                //data == response
            });
        });


        $(document).ready(function () {
            $.ajax({
                url: "dropdown_value.php",
                method: "GET",
                dataType: "JSON",
                success: function (data) {
                    // $("#courses").empty();

                    if (data[0] === 'empty') {
                        $("#courses").append("<li>No courses available.</li>");
                    } else {
                        $.each(data, function (key, value) {
                            const radioId = `course_${key}`;
                            $("#courses").append(`
                        <li class='flex items-center'>
                            <input id='apple' type='radio' value='${value.course}' name='course_box' class='w-4 h-4 bg-gray-100 border-gray-300 rounded text-primary-600 focus:ring-primary-500 dark:focus:ring-primary-600 dark:ring-offset-gray-700 focus:ring-2 dark:bg-gray-600 dark:border-gray-500'>
                            <label for='apple' class='ml-2 text-sm font-medium text-gray-900 dark:text-gray-100'>${value.course}</label>
                        </li>
                    `);
                        });

                        // according to dropdown
                        $('#courses input[type="radio"]').on('change', function () {
                            var selectedValue = $(this).val(); 
                            $.ajax({
                                url: "student_data_show.php", 
                                method: "POST",
                                data: { selectedValue: selectedValue }, 
                                success: function (response) {
                                    $('#result').html(response); 
                                }
                            })
                        });

                    }
                },
                error: function (xhr, status, error) {
                    console.error('Error fetching data: ', error);
                    alert('An error occurred while fetching courses. Please try again later.');
                }
            });
        });

    </script>

